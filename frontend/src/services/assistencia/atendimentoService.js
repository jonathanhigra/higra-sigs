import api from '../../lib/api';
const BASE = '/api/assistencia/atendimentos';
export const atendimentoService = {
  listar:            (params) => api.get(BASE, { params }),
  historicocliente:  (cliente, excludeId) => api.get(BASE, { params: { cliente, exclude_id: excludeId, per_page: 15 } }),
  timelineCliente:   (id) => api.get(`${BASE}/${id}/timeline-cliente`),
  timeline:          (id) => api.get(`${BASE}/${id}/timeline`),
  kanban:            (params) => api.get(`${BASE}/kanban`, { params }),
  kanbanMover:       (atnId, dados) => api.put(`${BASE}/kanban/mover/${atnId}`, dados),
  obter:             (id) => api.get(`${BASE}/${id}`),
  criar:             (dados) => api.post(BASE, dados),
  iniciar:           (atnId) => api.post(`${BASE}/${atnId}/iniciar`),
  formOptions:       () => api.get(`${BASE}/form-options`),
  adicionarEquipamento: (atnId, dados) => api.post(`${BASE}/${atnId}/equipamentos`, dados),
  uploadAnexo:       (atnId, formData) => api.post(`${BASE}/${atnId}/anexos`, formData, {
    headers: { 'Content-Type': 'multipart/form-data' },
  }),
  criarLaudo:        (atnId, dados) => api.post(`${BASE}/${atnId}/laudos`, dados),
  editarAnotacao:    (atnId, antId, dados) => api.put(`${BASE}/${atnId}/anotacoes/${antId}`, dados),
  vincularNegocio:   (atnId, dados) => api.post(`${BASE}/${atnId}/negocios`, dados),
  buscarLaudos:      (q) => api.get(`${BASE}/buscar-laudos`, { params: { q, limit: 20 } }),
  vincularLaudo:     (atnId, dados) => api.post(`${BASE}/${atnId}/laudos/vincular`, dados),
  buscarChecklists:  (q) => api.get(`${BASE}/buscar-checklists`, { params: { q, limit: 20 } }),
  vincularChecklist: (atnId, dados) => api.post(`${BASE}/${atnId}/checklists/vincular`, dados),
  buscarEmpresas:    (q) => api.get(`${BASE}/buscar-empresas`, { params: { q, limit: 30 } }),
  patchAtendimento:  (id, dados) => api.patch(`${BASE}/${id}`, dados),
  adicionarParticipante: (atnId, dados) => api.post(`${BASE}/${atnId}/participantes`, dados),
  removerParticipante:   (atnId, partId) => api.delete(`${BASE}/${atnId}/participantes/${partId}`),
  buscarUsuariosSigs:    (q) => api.get(`${BASE}/buscar-usuarios-sigs`, { params: { q, limit: 30 } }),
  listarRelacionados:    (atnId) => api.get(`${BASE}/${atnId}/relacionados`),
  vincularAtendimento:   (atnId, dados) => api.post(`${BASE}/${atnId}/relacionados`, dados),
  desvincularAtendimento:(atnId, relId) => api.delete(`${BASE}/${atnId}/relacionados/${relId}`),
  listarAtividades:      (atnId) => api.get(`/api/assistencia/${atnId}/atividades`),
  criarAtividade:        (atnId, dados) => api.post(`/api/assistencia/${atnId}/atividades`, dados),
  atualizarAtividade:    (atnId, ativId, dados) => api.patch(`/api/assistencia/${atnId}/atividades/${ativId}`, dados),
  listarRams:            (atnId) => api.get(`${BASE}/${atnId}/rams`),
  associarRam:           (atnId, dados) => api.post(`${BASE}/${atnId}/rams`, dados),
  desassociarRam:        (atnId, regId) => api.delete(`${BASE}/${atnId}/rams/${regId}`),
  listarPecas:           (atnId) => api.get(`${BASE}/${atnId}/pecas`),
  adicionarPeca:         (atnId, dados) => api.post(`${BASE}/${atnId}/pecas`, dados),
  removerPeca:           (atnId, pecaId) => api.delete(`${BASE}/${atnId}/pecas/${pecaId}`),
  custoResumo:           (atnId) => api.get(`${BASE}/${atnId}/custo-resumo`),
  solicitarAutorizacao:  (atnId, dados) => api.post(`${BASE}/${atnId}/solicitar-autorizacao`, dados),
  listarVinculosExt:     (atnId) => api.get(`${BASE}/${atnId}/vinculos-ext`),
  criarVinculoExt:       (atnId, dados) => api.post(`${BASE}/${atnId}/vinculos-ext`, dados),
  removerVinculoExt:     (atnId, vincId) => api.delete(`${BASE}/${atnId}/vinculos-ext/${vincId}`),
  bulkUpdate:            (payload) => api.post(`${BASE}/bulk-update`, payload),
  permissoes:            () => api.get('/api/assistencia/permissoes'),
};
